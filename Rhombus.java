
public class Rhombus {

	
	public static void main(String[] args) {
		
		for(int i=1;i<10;i++)
		{
			for(int j=9;j>1;j--){
				if(i<j){
					System.out.print(i);
				
			}}
			
			System.out.println();
		}
		
		for(int i=1;i<10;i++)
		{
			if(i<5)
			
			{
				System.out.print(" ");
				i++;}
			System.out.println(i);
		}
			
	}

}
